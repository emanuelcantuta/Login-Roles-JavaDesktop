    private void cargarTabla() {
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            } 
        };
    
        String titulos[] = {"Id", "Usuario", "Rol"};
        modeloTabla.setColumnIdentifiers(titulos);
       
        List<Usuario> usuarios = controladora.traerUsuarios();
        
        if (usuarios != null) {
            for(Usuario usu : usuarios) {
                Object[] objeto = {usu.getId(), usu.getNombreUsuario(), usu.getUnRol().getNombreRol()};
                
                modeloTabla.addRow(objeto);
            }
        }
        tablaUsuarios.setModel(modeloTabla);
        
        if (tablaUsuarios.getColumnModel().getColumnCount() > 0) {
            
            // ancho para campo (ID)
            tablaUsuarios.getColumnModel().getColumn(0).setPreferredWidth(50);
            tablaUsuarios.getColumnModel().getColumn(0).setMaxWidth(80); //maximo 80px
            
            // ancho para campo (USUARIO)
            tablaUsuarios.getColumnModel().getColumn(1).setPreferredWidth(250);
            
            // ancho para campo (ROL)
            tablaUsuarios.getColumnModel().getColumn(2).setPreferredWidth(150);
        }
    }
