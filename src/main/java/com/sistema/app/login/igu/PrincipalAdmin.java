    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (tablaUsuarios.getRowCount() > 0) {
            if (tablaUsuarios.getSelectedRow() != -1) {
                int idUsuario = Integer.parseInt(String.valueOf(tablaUsuarios.getValueAt(tablaUsuarios.getSelectedRow(), 0)));
                controladora.borrarUsuario(idUsuario);
                Mensajes.mostrar( "info", "Eliminación Correcta", "Se borro el usuario correctamente", this);
                cargarTabla();
            } else {
                Mensajes.mostrar("error", "Error al Borrar", "No selecciono ningún registro", this);
            }
        } else {
            Mensajes.mostrar("error", "Error al Borrar", "La tabla está vacia", this);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tablaUsuarios.getRowCount() > 0) {
            if (tablaUsuarios.getSelectedRow() != -1) {
                int idUsuario = Integer.parseInt(String.valueOf(tablaUsuarios.getValueAt(tablaUsuarios.getSelectedRow(), 0)));
                Navegacion.cambiarVentana(this, new EdicionUsuario(controladora, nombreUsuario, idUsuario));
            } else {
                Mensajes.mostrar("error", "Error al Editar", "No selecciono ningún registro", this);
            }
        } else {
            Mensajes.mostrar("error", "Error al Editar", "La tabla está vacia", this);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

